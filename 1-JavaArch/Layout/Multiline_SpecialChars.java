public class Multiline_SpecialChars {
    public static void main(String[] args) {
        System.out.println("Multiline print of special character");
        System.out.println("\':Single quotation mark"
                        +"\n\":Double quotation mark"
                        +"\n\\:Backslash"
                        +"\n\t:Tab"
                        +"\n\b:Backspace"
                        +"\n\r:Carriage return"
                        +"\n\f:Formfeed"
                        +"\n\n:Newline"
                        );
    }
}

